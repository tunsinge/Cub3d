int	mapX = 8, mapY = 5, mapS = 64;

void	draw_line(mlx_image_t *img, int x0, int y0, int x1, int y1, int color)
{
	int dx;
	int dy;
	int err;
	int e2;
	int	sx;
	int	sy;

	dx = abs(x1-x0);
	sx = x0<x1 ? 1 : -1;
	dy = abs(y1-y0);
	sy = y0<y1 ? 1 : -1;
	err = (dx>dy ? dx : -dy)/2;

	while (1)
	{
		if (x0 >= 0 && x0 <= windowWidth && y0 >= 0 && y0 <= windowHeight)
			mlx_put_pixel(img, x0, y0, color);
		else
			return ;
		if (x0==x1 && y0==y1) break;
		e2 = err;
		if (e2 >-dx) { err -= dy; x0 += sx; }
		if (e2 < dy) { err += dx; y0 += sy; }
	}
}

float dist (float ax, float ay, float bx, float by)
{
	return (sqrt((bx-ax) * (bx-ax) + (by-ay) * (by-ay)));
}

void	check_angle(float *ra)
{
	if (*ra < 0)
		*ra += 2*PI;
	if (*ra>2*PI)
		*ra -= 2*PI;
}

void	raycaster(t_cub3d *uwu)
{
	float	px, py, pa, rx, ry, ra, xo, yo,ray_nb,r,disT;
	int		mx,my,mp,dof;

	px = uwu->px + uwu->p_size / 2;
	py = uwu->py + uwu->p_size / 2;
	ra = uwu->pa-DR*30;
	pa = uwu->pa;
	check_angle(&ra);
	ray_nb = 60;
	for (r = 0; r < ray_nb; r++)
	{
		dof = 0;
		float disH=1000000, hx=px, hy=py;
		float aTan = -1/tan(ra);
		if (ra > PI)
		{
			ry = (((int)py/uwu->m_size)*uwu->m_size)-0.0001;
			rx = (py-ry)*aTan+px;
			yo = -uwu->m_size;
			xo = -yo*aTan;
		}
		if (ra < PI)
		{
			ry = (((int)py/uwu->m_size)*uwu->m_size) + uwu->m_size;
			rx = (py-ry)*aTan+px;
			yo = uwu->m_size;
			xo = -yo*aTan;
		}
		if (ra == 0 || ra == PI)
		{
			ry = py;
			rx = px;
			dof = 8;
		}
		while (dof < 8)
		{
			mx = (int)(rx)/uwu->m_size;
			my = (int)(ry)/uwu->m_size;
			if (mx+my*mapX > 0 && mx < mapX && my < mapY && uwu->map[my][mx] == '1')
			{
				hx=rx;
				hy=ry;
				disH=dist(px,py,hx,hy);
				dof = 8;
			}
			else
			{
				rx += xo;
				ry += yo;
				dof++;
			}
		}

		//HAHA

		dof = 0;
		float disV=1000000, vx=px, vy=py;
		float nTan = -tan(ra);
		if (ra > P2 && ra < P3)
		{
			rx = (((int)px/uwu->m_size)*uwu->m_size)-0.0001;
			ry = (px-rx)*nTan+py;
			xo = -uwu->m_size;
			yo = -xo*nTan;
		}
		if (ra < P2 || ra > P3)
		{
			rx = (((int)px/uwu->m_size)*uwu->m_size) + uwu->m_size;
			ry = (px-rx)*nTan+py;
			xo = uwu->m_size;
			yo = -xo*nTan;
		}
		if (ra == 0 || ra == PI)
		{
			ry = py;
			rx = px;
			dof = 8;
		}
		while (dof < 8)
		{
			mx = (int)(rx)/uwu->m_size;
			my = (int)(ry)/uwu->m_size;
			if (mx+my*mapX > 0 && mx < mapX && my < mapY && uwu->map[my][mx] == '1')
			{
				vx=rx;
				vy=ry;
				disV=dist(px,py,vx,vy);
				dof = 8;
			}
			else
			{
				rx += xo;
				ry += yo;
				dof++;
			}
		}

		float shade = 1;
		if (disV<disH)
		{
			shade = 0.5;
			rx=vx;
			ry=vy;
			disT = disV;
		}
		if (disV>disH)
		{
			rx=hx;
			ry=hy;
			disT = disH;
		}
		draw_line(uwu->ray_img, px, py, rx, ry, RED);

		//---Draw 3D Walls---
		float ca=pa-ra; if(ca<0) {ca+=2*PI;} if(ca>2*PI) {ca-=2*PI;} disT=disT*cosf(ca);
		float lineH=(mapS*320)/disT;
		float ty_step = 32.0/(float)lineH;
		float ty_off = 0;
		if(lineH>320){ty_off = (lineH-320)/2.0;lineH=320;}
		float lineO=160-lineH/2;

		//draw_texture(r*8+530, lineO, r*8+530, lineH+lineO, color);

		int y;
		float ty = ty_step*ty_off;
		float tx;

		if (shade == 1){ tx=(int)(rx/2.0)%32; if(ra<PI){ tx=31-tx ;} }
		else		   { tx=(int)(ry/2.0)%32; if (ra>P2 && ra<P3) { tx=31-tx; }}

		ty += 32*2;

		for (y = 0; y < lineH; y++)
		{
			float c = All_Textures[(int)(ty)*32 + (int)(tx)] * shade;
			int color = get_rgba(c*255,c*255,c*255, 255);
			for (int z = 0; z < 8; z++)mlx_put_pixel(uwu->ray_img, r*8+z, lineO+y, color);
			ty += ty_step;
		}

		for (int z = 0; z < 8; z++)
		{
			draw_line(uwu->ray_img, r*8+z, 0, r*8+z, lineO, CYA); // Plafond
			//draw_line(r*8+530+z, lineO, r*8+530+z, lineH+lineO, color); // Mur
			draw_line(uwu->ray_img, r*8+z, lineH+lineO, r*8+z, 320, GRE); // Sol
		}

		ra+=DR*(60 / ray_nb);
		check_angle(&ra);
	}
}
